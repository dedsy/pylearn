# ints:
print(1)
print(-1)

print(1 + 2)
print(5 * 9)























# floats:
print(3.14)
print(3.14000)
print(-3.14)

print(1.3 * 0.3)
print(1.3 * 0.3)
print(279 * 0.105)

# mixed:
print(1 + 1.5)
print(3 * 0.5)












# complex:
print(1j * 1j)  # 1j = sqrt(-1)






# division:
print(4 / 3)
print(4 // 3)
print(4 % 3)













# overfloating int:
import sys  # importing the standart library `sys`.

print(sys.maxsize)

print(sys.maxsize + 1)

print(-sys.maxsize)
print(-sys.maxsize - 1)
print(-sys.maxsize - 2)



# floating point math:
print(0.1 + 0.2)

